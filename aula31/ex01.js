// Criando arrow functions para operações básicas de matemática
let soma = (a, b) => a + b
let subtracao = (a, b) => a - b
let divisao = (a, b) => a / b
let multiplicacao = (a, b) => a * b

console.log(soma(5, 5))
console.log(subtracao(5, 4))
console.log(divisao(10, 2))
console.log(multiplicacao(5, 2))
